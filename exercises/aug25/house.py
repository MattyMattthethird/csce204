import turtle
turtle.bgcolor("Light pink")

#setup pen
pen = turtle.Turtle()
pen.pensize(2) #change the width of pen
pen.color("Royal blue")
pen.hideturtle()  #hides the cursor

#draw the house frame 
pen.setposition(0,0)
pen.right(100)


#draw the roof

turtle.done()  #must be the last line of the file
